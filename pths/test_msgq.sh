#!/bin/bash

# Скрипт тестирования программы с очередями сообщений

echo "================================================"
echo "  ТЕСТ: pthread + Message Queue"
echo "================================================"
echo ""

# Очистка старых очередей
echo "🧹 Очистка старых очередей сообщений..."
ipcs -q | grep $(whoami) | awk '{print $2}' | xargs -I {} ipcrm -q {} 2>/dev/null
echo ""

# Компиляция
echo "📦 Компиляция программы..."
gcc -o itg.out itg.c -lm -lpthread 2>&1
if [ $? -eq 0 ]; then
    echo "✅ Компиляция успешна"
else
    echo "❌ Ошибка компиляции"
    exit 1
fi
echo ""

echo "================================================"
echo "  Режим непрерывной функции f(x) = sqrt(x)"
echo "================================================"
echo ""

# Тест 1
echo "🧪 Тест 1: ∫[0,4] sqrt(x)dx (ожидается ~5.33)"
echo "---"
./itg.out cont 0 4 0.01
echo ""
echo ""

# Тест 2
echo "🧪 Тест 2: ∫[1,9] sqrt(x)dx (ожидается ~17.33)"
echo "---"
./itg.out cont 1 9 0.001
echo ""
echo ""

# Тест 3
echo "🧪 Тест 3: ∫[1,16] sqrt(x)dx (ожидается ~42.00)"
echo "---"
./itg.out cont 1 16 0.001
echo ""
echo ""

echo "================================================"
echo "  Режим дискретных данных"
echo "================================================"
echo ""

# Тест 4
echo "🧪 Тест 4: Дискретные данные (5 точек)"
echo "---"
./itg.out 5 0 1 2 3 4 0 1 1.414 1.732 2 1 5 0.01
echo ""
echo ""

# Финальная очистка
echo "🧹 Финальная очистка очередей..."
ipcs -q | grep $(whoami) | awk '{print $2}' | xargs -I {} ipcrm -q {} 2>/dev/null
echo ""

echo "================================================"
echo "  ТЕСТИРОВАНИЕ ЗАВЕРШЕНО"
echo "================================================"
echo ""
echo "✅ Все тесты выполнены!"
echo ""
echo "Проверить оставшиеся очереди:"
echo "  ipcs -q"
echo ""


